package com.alltoons.root.webtoon.dto;

public class WebtoonViewDTO {
	public String webtoonTitle;
	public int webtoonNum;
	public String webtoonWriter;
	public String webtoonContent;
	public String webtoonImage;
	public String webtoonGenre;
	
	public String getWebtoonTitle() {
		return webtoonTitle;
	}
	public void setWebtoonTitle(String webtoonTitle) {
		this.webtoonTitle = webtoonTitle;
	}
	public int getWebtoonNum() {
		return webtoonNum;
	}
	public void setWebtoonNum(int webtoonNum) {
		this.webtoonNum = webtoonNum;
	}
	public String getWebtoonWriter() {
		return webtoonWriter;
	}
	public void setWebtoonWriter(String webtoonWriter) {
		this.webtoonWriter = webtoonWriter;
	}
	public String getWebtoonContent() {
		return webtoonContent;
	}
	public void setWebtoonContent(String webtoonContent) {
		this.webtoonContent = webtoonContent;
	}
	public String getWebtoonImage() {
		return webtoonImage;
	}
	public void setWebtoonImage(String webtoonImage) {
		this.webtoonImage = webtoonImage;
	}
	public String getWebtoonGenre() {
		return webtoonGenre;
	}
	public void setWebtoonGenre(String webtoonGenre) {
		this.webtoonGenre = webtoonGenre;
	}
}
